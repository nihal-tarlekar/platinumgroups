// about 1
$(document).ready(function(){
    $('#slider-1').click(function(e) {
    $('#sign_up').lightbox_me({
    centered: true, 
    onLoad: function() { 
    $('#sign_up').find('input:first').focus()
    }
    });
    e.preventDefault();
    });

    $ele.lightbox_me();
    $ele.trigger('close');
})
